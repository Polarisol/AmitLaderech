static int pMain;

int main (int argc, char *argv[])
{
	if (InitCVIRTE (0, argv, 0) == 0)
		return -1;	/* out of memory */
	if ((pMain = LoadPanel (0, "1.uir", P_MAIN)) < 0)
		return -1;
	DisplayPanel (pMain);
	RunUserInterface ();
	DiscardPanel (pMain);
	return 0;
}

